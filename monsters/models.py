from django.db import models


class Monster(models.Model):
    name = models.CharField(max_length=50, unique=True)
    size = models.CharField(max_length=20, null=True, blank=True)
    type = models.CharField(max_length=20, null=True, blank=True)
    alignment = models.CharField(max_length=20, null=True, blank=True)
    ac = models.IntegerField(null=True, blank=True)
    hp = models.CharField(max_length=12, null=True, blank=True)
    speed = models.TextField(null=True, blank=True)
    str_mod = models.IntegerField(null=True, blank=True)
    dex_mod = models.IntegerField(null=True, blank=True)
    con_mod = models.IntegerField(null=True, blank=True)
    int_mod = models.IntegerField(null=True, blank=True)
    wis_mod = models.IntegerField(null=True, blank=True)
    cha_mod = models.IntegerField(null=True, blank=True)
    saving_throws = models.TextField(null=True, blank=True)
    skills = models.TextField(null=True, blank=True)
    vulnerabilies = models.TextField(null=True, blank=True)
    resistances = models.TextField(null=True, blank=True)
    immunities = models.TextField(null=True, blank=True)
    senses = models.TextField(null=True, blank=True)
    languages = models.TextField(null=True, blank=True)
    cr = models.FloatField(null=True, blank=True)
    xp = models.IntegerField(null=True, blank=True)
    traits = models.TextField(null=True, blank=True)
    actions = models.TextField(null=True, blank=True)
    legendary_actions = models.TextField(null=True, blank=True)
